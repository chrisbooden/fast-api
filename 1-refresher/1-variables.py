"""
Variables
"""

first_name = "Eric"
print(first_name)

last_name = "Melissa"
print(last_name)

full_name = first_name + " " + last_name
print(full_name)

x = [1, 2, 3, 4, 5]
print(x[0])

print(x[-1])

print(x[1:3])       # 2nd to 4th element
print(x[0::2])      # Step of 2
print(x[-1::-1])    # Reverse step of -1
print(x[-1::-2])    # Reverse step of -2
print(x[-2::-1])    # Reverse step of -1 from second to last element